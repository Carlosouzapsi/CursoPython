def aumentar(preco, aut):
    res = preco + ((preco * aut)/100)
    return res

def diminuir(preco, sub):
    res = preco - ((preco * sub)/100)
    return res

def dobro(preco):
    dobroPreco = preco * 2
    return dobroPreco


def metade(preco):
    metadePreco = preco / 2
    return metadePreco

